    public static final String YYYYMMDD_HH_MM_SS = "yyyyMMdd HH:mm:ss";

    public static final String YYYYMMDD_HHMMSS = "yyyyMMdd HH:mm:ss";

    public static final String YYYYMMDDHHMMSS = "yyyyMMddHH:mm:ss";

    public static final String YYYY_MM_DD = "yyyy-MM-dd";

    public static final String YYYYMMDD = "yyyyMMdd";

    private static final Map<Integer, SimpleDateFormat> DATE_TIME_FORMAT_MAP = new HashMap<>(8);

    static {
    	DATE_TIME_FORMAT_MAP.put(8, new SimpleDateFormat(YYYYMMDD));
    	DATE_TIME_FORMAT_MAP.put(10, new SimpleDateFormat(YYYY_MM_DD));
    	DATE_TIME_FORMAT_MAP.put(14, new SimpleDateFormat(YYYYMMDDHHMMSS));
    	DATE_TIME_FORMAT_MAP.put(15, new SimpleDateFormat(YYYYMMDD_HHMMSS));
    	DATE_TIME_FORMAT_MAP.put(17, new SimpleDateFormat(YYYYMMDD_HH_MM_SS));
    	DATE_TIME_FORMAT_MAP.put(19, new SimpleDateFormat(YYYY_MM_DD_HH_MM_SS));
    }

    public static ThreadLocal<SimpleDateFormat> dateFormatThreadLocal = new ThreadLocal<SimpleDateFormat>() {
        @Override
        protected SimpleDateFormat initialValue() {
            return DATE_TIME_FORMAT_MAP.get(19);
        }
    };

    public static String getDateFormatStr() {
        return dateFormatThreadLocal.get().toPattern();
    }

    public static SimpleDateFormat getDateFormat() {
        return dateFormatThreadLocal.get();
    }

    public static void setDateFormatStr(String dateFormatStr) {
        dateFormatThreadLocal.get().applyPattern(dateFormatStr);
    }

    public static String formatDate(Date date) {
        return dateFormatThreadLocal.get().format(date);
    }

    public static String formatDate(Date date, String format) {
        setDateFormatStr(format);
        return formatDate(date);
    }

    public static Date parseDate(String dateTimeStr) throws ParseException {
        SimpleDateFormat simpleDateFormat = DATE_TIME_FORMAT_MAP.get(dateTimeStr.length());
        if (null == simpleDateFormat) {
            throw new RuntimeException(
                    "the format of dateTimeStr is not support this method, please use parseDate(String, String)");
        }
        return simpleDateFormat.parse(dateTimeStr);
    }

    public static Date parseDate(String dateTimeStr, String sourceDateFormat) throws ParseException {
        return new SimpleDateFormat(sourceDateFormat).parse(dateTimeStr);
    }

